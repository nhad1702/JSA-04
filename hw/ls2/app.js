function remainder(a, b) {
    let div = a % b;
    return div;
}



function findPerimeter(a, b) {
    let peri = (a + b) * 2;
    return peri;
}



function points(a, b) {
    let sum = (a * 2) + (b * 3);
    return sum;
}




function animals(a, b, c) {
    let ani = (a * 2) + (b * 4) + (c * 4);
    return ani;
}




function footballPoints(a, b, c) {
    let football = (a * 3) + (b * 1) + (c * 0);
    return football;
}



function sumPolygon(n) {
    let sum = (n - 2) * 180;
    return sum;
}




